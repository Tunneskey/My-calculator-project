var opr= prompt('Please Enter operator; e.g +,-,*,/');
var num1= parseInt(prompt('number 1: '));
var num2= parseInt(prompt('number 2: '));

if(opr == "+")
{
    alert(num1 + num2)
}else if(opr == "-")
{
    alert(num1 - num2)  
}else if(opr == "*")
{
    alert(num1 * num2)  
}else if(opr == "/")
{
    alert(num1 / num2)
}

